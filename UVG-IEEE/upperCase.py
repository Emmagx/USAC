#Formato de cadena el que hace que la cadena se vuelva mayusculas.
cadena = 'hola mundo'
print(cadena.upper()) # HOLA MUNDO

cadena = cadena.upper()
print(cadena) # HOLA MUNDO

if cadena == 'HOLA MUNDO':
    print('la cadena es igual')
else:
    print('La cadena no es igual')
    
cadena = 'hola mundo'
if cadena == 'HOLA MUNDO':
    print('la cadena es igual')
else:
    print('La cadena no es igual')